                             <h2 class="section-heading text-left">About the Artist.</h2>
                                          <p> 
Roberto Petrosino, Italian, was born on the (ten) day of June in the year one thousand nine hundred and seventy six in the city of Milan. He has technical and scientific training. He landed in Maceió, the capital of Alagoas in Brazil in 2003 for work reasons, where he lives to this day in a very wooded neighborhood bordering rural areas.</p>
<p>Inspired by local artists and artisans, he approaches the world of mercenary, but his passion for art and design makes each piece of art unique. He performs his marvelous arts by rescuing trees that are felled for reasons of urban expansion, as well as those that fall or are killed, taking advantage of one hundred percent of them, using branches, roots and stems, thus exerting a sustainable work. Among the most commonly used species: jacks, eucalyptus, almonds, sapotizeiros, fruit-breads and acacias, which are of exotic origin, which do not need the DOF (Document of Forest Origin).</p>
<p>In the works of the artist Roberto Petrosino, in addition to the concern to revive the rescued vegetative species, giving them a new meaning with a more noble destiny, the same seeks to transpire in each piece the will of the approximation between man and wood this wonderful matter -prima through interaction with environment and its use.</p>
<p>Its concept of work is thus defined, preserving beauty as much as possible in the forms which have been imprinted by nature.</p>                            </div>

